# січень, лютий, березень, квітень, травень, червень, липень, серпень, вересень, жовтень, листопад, грудень.

month = int(input("Введіть число місяця (1-12): "))

match month:
    case 1:
        print("Січень")
    case 2:
        print("Лютий")
    case 3:
        print("Березень")
    case 4:
        print("Квітень")
    case 5:
        print("Травень")
    case 6:
        print("Червень")
    case 7:
        print("Липень")
    case 8:
        print("Серпень")
    case 9:
        print("Вересень")
    case 10:
        print("Жовтень")
    case 11:
        print("Листопад")
    case 12:
        print("Грудень")